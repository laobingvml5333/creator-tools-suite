/**
 * Find the most relevant category based on user input
 * @param {string} input - User's video title or description
 * @returns {string|null} - Category name or null if no match
 */
window.findCategory = function (input) {
    if (!input || typeof input !== 'string') return null;

    const lowerInput = input.toLowerCase();
    const words = lowerInput.split(/\s+/);

    // Count matches for each category
    const categoryScores = {};

    // Check each word against keyword map
    for (const word of words) {
        for (const [keyword, category] of Object.entries(window.KEYWORD_CATEGORY_MAP)) {
            if (lowerInput.includes(keyword)) {
                categoryScores[category] = (categoryScores[category] || 0) + 1;
            }
        }
    }

    // Return category with highest score
    if (Object.keys(categoryScores).length > 0) {
        return Object.entries(categoryScores)
            .sort((a, b) => b[1] - a[1])[0][0];
    }

    return null;
};

