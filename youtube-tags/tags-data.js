/**
 * Generate tags based on user input and optional category
 * @param {string} input - User's video title or description
 * @param {string} selectedCategory - Optional manually selected category
 * @returns {Object} - { tags: Array, category: string, score: number }
 */
window.generateTags = function (input, selectedCategory = null) {
    let category = selectedCategory;

    // If no category selected, try to detect it
    if (!category || category === '') {
        category = window.findCategory(input);
    }

    let tags = [];

    // Get category-specific tags
    if (category && window.TAG_DATABASE[category]) {
        tags = [...window.TAG_DATABASE[category]];
    }

    // Add universal tags
    tags = [...tags, ...window.UNIVERSAL_TAGS.slice(0, 10)];

    // Remove duplicates
    tags = [...new Set(tags)];

    // Calculate relevance score (0-100)
    const score = category ? 85 : 60;

    return {
        tags: tags,
        category: category || 'general',
        score: score
    };
};

